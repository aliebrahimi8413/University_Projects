# کلاس گاری (همون چیزی که پاندول روش وایساده و چپ و راست میره)

class Cart:
    def __init__(self):
        self.position = 0.0   # مکان گاری روی ریل (متر) - صفر یعنی وسط
        self.speed = 0.0      # سرعت گاری

        self.mass = 1.0       # جرم گاری (کیلوگرم)

        self.track_limit = 2.4  # اگه از این بیشتر بره، یعنی از ریل خارج شده (باخت)
